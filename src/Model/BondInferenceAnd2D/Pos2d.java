package Model.BondInferenceAnd2D;

/**
 * Created by Philipp on 2016-01-30.
 * 2d position
 */
public class Pos2d {

    private double x;
    private double y;

    public Pos2d(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
