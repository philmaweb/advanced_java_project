package Model.BondInferenceAnd2D;

/**
 * Created by Philipp on 2016-01-30.
 * 2d position
 */
public class Pos2d {

    private int x;
    private int y;

    public Pos2d(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
